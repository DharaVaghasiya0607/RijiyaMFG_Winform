        private void BtnRefresh_Click(object sender, EventArgs e)
        {
            try
            {
                this.Cursor = Cursors.WaitCursor;

                string StrOpe = "";

                if (Val.ToString(txtEmployee.Text).Trim().Equals(string.Empty))
                    txtEmployee.Tag = string.Empty;

                if (RbtAll.Checked == true)
                {
                    StrOpe = Val.ToString(RbtAll.Tag);
                }
                else if (RbtPass.Checked == true)
                {
                    StrOpe = Val.ToString(RbtPass.Tag);
                }
                else if (RbtFail.Checked == true)
                {
                    StrOpe = Val.ToString(RbtFail.Tag);
                }

                DataSet DS = Obj.GetEmployeeOverDueReport(StrOpe, Val.ToInt64(txtEmployee.Tag), Val.Trim(CmbKapan.Properties.GetCheckedItems()));

                MainGridSummary.DataSource = DS.Tables[0];
                GrdDetSummary.RefreshData();
                GrdDetSummary.BestFitColumns();

                MainGridKapan.DataSource = DS.Tables[1];
                GrdDetKapan.RefreshData();
                GrdDetKapan.BestFitColumns();


                MainGridPacketDetail.DataSource = DS.Tables[2];
                GrdDetPacketDetail.RefreshData();
                GrdDetPacketDetail.BestFitColumns();

                GrdDetPacketDetail.Columns["TOEMPLOYEECODE"].ClearFilter();
                GrdDetPacketDetail.Columns["TODEPARTMENTNAME"].ClearFilter();
                GrdDetPacketDetail.Columns["KAPANNAME"].ClearFilter();

                this.Cursor = Cursors.Default;
            }
            catch (Exception EX)
            {
                this.Cursor = Cursors.Default;
                Global.Message(EX.ToString());
            }

        }

